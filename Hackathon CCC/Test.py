def auction_with_history(input_data):
    bids = input_data.split(',')
    
    current_price = int(bids[0])
    highest_bidder = None
    
    bid_history = []

    for i in range(1, len(bids), 2):
        bidder = bids[i]
        bid_amount = int(bids[i + 1])
        
        if bid_amount > current_price:
            current_price = bid_amount
            highest_bidder = bidder
            bid_history.append((bidder, current_price))
    
    bid_history.insert(0, ('-', int(bids[0])))
    
    history_str = ', '.join([f"{bidder}, {price}" for bidder, price in bid_history])
    return history_str

